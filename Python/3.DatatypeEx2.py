# float-dtype
a = 1.5
print(a)
print(type(a))

a = 1.4E3
print(a)
print(type(a))

a = 1.5E-3
print(a)
print(type(a))

a = 5+6j
print(a)
print(type(a))

a = 5+6.5j
print(a)
print(type(a))

a = 1.5+8.7j
print(a)
print(type(a))

a = True
print(a)
print(type(a))

a = False
print(a)
print(type(a))


